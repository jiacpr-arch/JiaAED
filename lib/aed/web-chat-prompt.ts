function productBlock(): string {
  return products
    .map(
      (p) =>
        `• ${p.name} (${p.subtitle}) — id: "${p.id}" — ฿${p.price.toLocaleString("th-TH")} (เริ่มต้น ยังไม่รวม VAT)\n  ${p.description} · ${p.features.join(" · ")}`,
    )
    .join("\n\n");
}

function faqBlock(): string {
  return faqs.map((f) => `Q: ${f.question}\nA: ${f.answer}`).join("\n\n");
}


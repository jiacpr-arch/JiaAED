import { listProducts, formatThaiPrice } from "./pricing";

function productBlock(): string {
  return listProducts()
    .map(
      (p) =>
        `• ${p.nameTh} (id: "${p.id}")\n  ราคาเริ่มต้น: ${formatThaiPrice(p.startingPrice)}\n  รายละเอียด: ${p.description}`,
    )
    .join("\n\n");
}

